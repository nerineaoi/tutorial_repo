function saludar(nombre) {
  console.log("Hola " + nombre) //Básica: console.log("Hola Miguel")
}
saludar("Miguel") // -> Hola Miguel 
// En el caso básico se pone solo saludar() y en este ejemplo se pone con el nombre entre comillas y dirá "Hola Miguel"
saludar("Ana") // -> Hola Ana
saludar("Luis") // -> Hola Luis